				<h1>A Thief on the Sidewalk</h1>
				<p>  She looked at the man walking along the sidewalk next to the nice houses. He didn't live in any of those houses. She had seen this man several times before. He looked like a criminal. She knew not to judge a book by its cover, but this man was no good. She drove by him. He looked at her car as she drove by. She continued driving. She watched him in her rear view mirror. She got to the corner and stopped. She continued to watch him walking in her direction. Suddenly, he turned left up a driveway. A red SUV was parked in the driveway right next to the sidewalk. She saw him walk up to the driver's door. He returned to the sidewalk and continued walking in her direction. What was that all about, she wondered. Then she realized that he had tested the door to see if it was locked. He is a criminal, she thought. That's what he does. He just walks through our neighborhoods looking for cars to break into. She called the police. She described him. The officer said he knew who the man was. Residents called two or three times a week to report him walking by. But the police couldn't arrest him for walking around. They had to catch him with stolen goods. "But he was testing that SUV door to see if it was unlocked," she said. "I'm sorry," said the officer. "That's not against the law. If you see him actually steal something, give us a call."  </p>
