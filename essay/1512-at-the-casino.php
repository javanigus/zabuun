				<h1>At the Casino</h1>
				<p>  She went to the casino. A casino is a gambling house. People gamble in a casino. They take a chance with their money. They hope they will win. Sometimes they do. But usually they lose. It was her birthday. She took $100 to the casino. She went to the change booth. She got change for her money. She got 400 quarters for her $100. The quarters were in paper rolls. Forty quarters were in each roll. She put the rolls into a plastic bucket. She carried the bucket over to a slot machine. She loved slot machines. She opened a roll of quarters. She put a quarter in the machine. She pulled down on the handle. She looked at the screen. Give me three cherries, she thought. Or give me three bananas. Or give me three apples. Three of anything is a winner. Two cherries, or one cherry, is a loser. She played for several hours. Sometimes she won. She felt good when she won. But usually she lost. Finally, she lost all her quarters. She went home. She spent $100, but she had fun. I will play again on my next birthday, she thought.  </p>
